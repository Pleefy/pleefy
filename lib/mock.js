export function aggregateByAgent(data){
  const map = {};
  for (const c of data){
    if(!map[c.agent]) map[c.agent] = { agent: c.agent, up:0, down:0, total:0 };
    map[c.agent].total++;
    if (c.thumbs==="up") map[c.agent].up++; else map[c.agent].down++;
  }
  return Object.values(map).sort((a,b)=>b.total-a.total);
}
export function kpi(data){
  const total = data.length;
  const success = data.filter(d=>d.thumbs==="up").length;
  const successRate = total ? Math.round((success/total)*100) : 0;
  const avgDuration = total ? Math.round(data.reduce((s,d)=>s+d.duration,0)/total) : 0;
  return { total, success, successRate, avgDuration };
}
